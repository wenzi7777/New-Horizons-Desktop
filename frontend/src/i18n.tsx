const dictionaries: Record<Locale, Dictionary> = {
  ...baseDictionaries,
  "zh-CN": zhCnDictionary,
};

const LOCALE_STORAGE_KEY = "newhorizons-desktop-language";

function normalizeLocale(input: string | null | undefined): Locale {
  const value = String(input || "").trim().toLowerCase();
  if (value === "ja" || value.startsWith("ja-")) return "ja";
  if (value === "zh" || value === "zh-cn" || value === "zh-hans" || value.startsWith("zh-cn") || value.startsWith("zh-hans")) {
    return "zh-CN";
  }
  return "en";
}

function detectBrowserLocale(): Locale {
  if (typeof navigator === "undefined") return "en";
  const candidates = Array.isArray(navigator.languages) && navigator.languages.length > 0
    ? navigator.languages
    : [navigator.language];
  for (const candidate of candidates) {
    const locale = normalizeLocale(candidate);
    if (candidate && locale) return locale;
  }
  return "en";
}

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    if (typeof window === "undefined") return "en";
    const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    return saved ? normalizeLocale(saved) : detectBrowserLocale();
  });

  useEffect(() => {
    if (typeof window === "undefined") return;
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    return {
      locale,
      setLocale: (nextLocale: Locale) => setLocale(normalizeLocale(nextLocale)),
      t: (key: string) => dictionaries[locale][key] ?? dictionaries.en[key] ?? key,
    };
  }, [locale]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("i18n_provider_missing");
  }
  return context;
}

export function commandDescriptionKey(command: string) {
  const normalized = command.replace(/-([a-z])/g, (_, char: string) => char.toUpperCase());
  return `terminalHelp${normalized.charAt(0).toUpperCase()}${normalized.slice(1)}`;
}
